import mongoose from 'mongoose';

export const userSchema = new mongoose.Schema({
    firstname: {
        type: String,
        required: true
    },
    lastname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    cart: Array,
    address: String
});

export const User = mongoose.model('User', userSchema);
